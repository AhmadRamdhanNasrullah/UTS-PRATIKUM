# Program untuk menggabungkan dua kata tanpa spasi

# Meminta pengguna memasukkan dua kata
kata1 = input("Masukkan kata pertama: ")
kata2 = input("Masukkan kata kedua: ")

# Menggabungkan kedua kata tanpa spasi
gabungan = kata1 + kata2

# Menampilkan hasil penggabungan
print("Hasil penggabungan kedua kata tanpa spasi:", gabungan)

