# Konversi Suhu Celcius ke Fahrenheit
celcius = float(input("Masukkan suhu dalam Celcius: "))


fahrenheit = (celcius * 9/5) + 32

print(f"Suhu dalam Fahrenheit: {fahrenheit}")
