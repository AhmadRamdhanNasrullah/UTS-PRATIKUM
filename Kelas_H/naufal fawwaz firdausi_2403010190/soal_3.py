# Program untuk menggabungkan dua kata menjadi satu string tanpa spasi

# Meminta input dua kata dari pengguna
kata1 = input("Masukkan kata pertama: ")
kata2 = input("Masukkan kata kedua: ")

# Menggabungkan kedua kata menjadi satu string tanpa spasi
gabungan = kata1 + kata2

# Menampilkan hasil gabungan
print(f"Hasil gabungan kedua kata: {gabungan}")
