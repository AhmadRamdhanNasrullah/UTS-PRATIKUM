# Program untuk mengonversi suhu dari Celcius ke Fahrenheit

# Meminta input suhu dalam Celcius dari pengguna
celcius = float(input("Masukkan suhu dalam Celcius: "))

# Mengonversi suhu Celcius ke Fahrenheit
fahrenheit = (celcius * 9/5) + 32

# Menampilkan hasil konversi
print(f"{celcius} derajat Celcius setara dengan {fahrenheit} derajat Fahrenheit.")
