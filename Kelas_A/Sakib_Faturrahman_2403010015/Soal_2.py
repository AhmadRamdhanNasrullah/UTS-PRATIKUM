#disini saya menghitung konversi celcius ke fahrenheit dengan menggunakan bahasa pemrograman python 
# dengan inputan suhu dalam celcius dari user menggunakan tipe data float

suhu_celcius = input("Masukkan suhu dalam suhu_celcius: ")

suhu_fahrenheit = float(suhu_celcius) * 9/5 + 32

print("Suhu dalam fahrenheit: ", suhu_fahrenheit)