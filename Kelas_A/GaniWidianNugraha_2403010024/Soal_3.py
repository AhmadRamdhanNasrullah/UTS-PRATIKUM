#buatlah program yang meminta pengguna untuk memasukkan dua kata,
# lalu menggabungkan kedua kata tersebut menjadi satu string baru tanpa spasi.

#Masukan input user
Kata_pertama = input("Masukan Kata pertama: ")
Kata_kedua = input ("Masukan Kata kedua: ")

#proses penggabungan kata

Gabungkan_kata =Kata_pertama + Kata_kedua

#output hasil dari penggabungan 
print(f"Hasil pengabungan: {Gabungkan_kata}")