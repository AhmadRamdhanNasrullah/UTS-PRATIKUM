# Program untuk mengonversi suhu dari Celcius ke Fahrenheit

#input suhu dalam celcius
suhu_celcius = float(input("masukan suhu dalam celcius: "))

#conversi celcius ke fahrenheit
suhu_fahrenheit = (suhu_celcius *(9/5))+ 32

#menampikan hasil conversi
print(f"suhu dalam fahrenhet:{suhu_fahrenheit}")
