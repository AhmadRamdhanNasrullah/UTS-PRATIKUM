## Soal 1: Menghitung Luas dan Keliling Persegi Panjang

# Input dari pengguna dan mengonversi ke tipe float
panjang = float(input("Masukkan nilai panjang: "))
lebar = float(input("Masukkan nilai lebar: "))

# Menghitung luas dan keliling
luas = panjang * lebar
keliling = 2 * (panjang + lebar)

# Menampilkan hasil
print("Luas persegi panjang yaitu: ", luas)
print("Keliling persegi panjang yaitu: ", keliling)