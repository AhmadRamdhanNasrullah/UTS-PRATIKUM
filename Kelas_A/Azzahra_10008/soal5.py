## Soal 5: Memotong Substring dari Sebuah Kata

# Input kata atau kalimat
kata = input("Masukkan kata: ")

# Input indeks awal
indeks_awal = int(input("Masukkan indeks awal: "))

# Input indeks akhir
indeks_akhir = int(input("Masukkan indeks akhir: "))

# Hasil
print("Hasil potongan pada kata: ", kata[indeks_awal:indeks_akhir])