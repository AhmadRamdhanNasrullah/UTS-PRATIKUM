# Menghitung Luas dan Keliling Persegi panjang

#variabel input panjang
panjang = float(input("input Panjang: "))

#variabel input lebar
lebar = float(input("input Lebar: "))

#variabel rumus luas
Luas = panjang * lebar

#variabel rumus keliling
keliling = 2 * (panjang + lebar)

#menampilkan hasil luas persegi panjang
print(panjang, "*", lebar, "Luas Persegi Panjang =", Luas)

#menampilkan hasil keliling persegi panjang
print( "2 * (",panjang, "+", lebar, ")", "keliling Persegi Panjang =", keliling)

